    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
                // TODO add your handling code here:
        Double amount = Double.parseDouble(txtAmount.getText()) ;
        String description = txtDescription.getText();
        String status = "pending";
        
        if (validateFields("new")){
            JOptionPane.showMessageDialog(null,"All fields are required");
        }
        else {
            try{
                String today = new  SimpleDateFormat("YYYY-MM-dd").format(Calendar.getInstance().getTime());
                String thisMonth = new  SimpleDateFormat("MM").format(Calendar.getInstance().getTime());
                String thisYear = new  SimpleDateFormat("YYYY").format(Calendar.getInstance().getTime());
                int payrollID;

                SQLRun objSQLRun = new SQLRun();
                String sql;
                ResultSet rs;
                
                sql = "SELECT * FROM payroll WHERE month(payroll_date)='"+thisMonth +"' AND year(payroll_date) ='"+thisYear +"' AND payroll_employee_id ='"+ employeeID+"';";
                rs = objSQLRun.sqlQuery(sql);

                if (rs.isBeforeFirst()){
                    rs.next();
                    payrollID = rs.getInt("payroll_id");
                    sql = "INSERT INTO "
                            + "allowance(allowance_amount, allowance_description, allowance_status,allowance_payroll_id) "
                            + "VALUE ('"+amount+"','"+description+"','" +status+"','" +payrollID +"')";
                }

               
                objSQLRun.sqlUpdate(sql);
                JOptionPane.showMessageDialog(null,"Added Sucessfully");
                btnResetActionPerformed(evt);

            }
            catch (Exception e){
                JOptionPane.showMessageDialog(null,e);
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

