    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        SQLRun objSQLRun = new SQLRun();
        String sql;
        ResultSet rs;
        
        sql = "SELECT * FROM allowance,payroll WHERE payroll_employee_id = '"+employeeID+"' AND payroll_id = allowance_payroll_id";
        rs = objSQLRun.sqlQuery(sql);
        
        try {
            while (rs.next()){
                model.addRow(new Object[]{rs.getString("allowance_id"),rs.getString("allowance_amount"),rs.getString("allowance_description"),rs.getString("allowance_status")});
            }
        } catch (SQLException ex) {
            Logger.getLogger(AllowanceRequest.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        btnDelete.setEnabled(false);
        btnUpdate.setEnabled(false);
        txtStatus.setEnabled(false);
    }//GEN-LAST:event_formComponentShown

