    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        //get input
        if (validateFields("edit")){
            JOptionPane.showMessageDialog(null,"All fields are required");
        }
        else{
            try {
                int id = Integer.parseInt(txtID.getText());
                String department = txtAmount.getText();
                String position = txtDescription.getText();
                int salary = Integer.parseInt(txtStatus.getText());
                
                //Init sql connection
                SQLRun objSQLRun = new SQLRun();
                String sql;
                ResultSet rs;
                
                sql = "SELECT department_id from department WHERE department_name ='"+ department+"';";
                rs = objSQLRun.sqlQuery(sql);
                System.out.println(sql);
                rs.next();
                int deparment_id = rs.getInt(1);
                
                sql = "SELECT position_id from position WHERE position_name ='"+ position+"';";
                rs = objSQLRun.sqlQuery(sql);
                rs.next();
                int position_id = rs.getInt(1);
                
                
                //Run sql
                sql = "UPDATE job "
                        + "SET job_basic_salary = '" + salary
                        + "' ,job_department_id = '" + deparment_id
                        + "' ,job_position_id = '" + position_id
                        + "' WHERE job_id = '" + id + "'";
                
                System.out.println(sql);
                objSQLRun.sqlUpdate(sql);
                JOptionPane.showMessageDialog(null,"Updated Sucessfully"); 
                btnResetActionPerformed(evt);
            } catch (SQLException ex) {
                Logger.getLogger(AllowanceRequest.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_btnUpdateActionPerformed

    private void jTable1MouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseReleased
        // TODO add your handling code here:
                // TODO add your handling code here:
        int index = jTable1.getSelectedRow();
        TableModel model = jTable1.getModel();
        
        String id = model.getValueAt(index,0).toString();
        selectedID = Integer.parseInt(id);
        txtID.setText(id);
        
        String amount = model.getValueAt(index,1).toString();
        txtAmount.setText(amount);
        
        String description = model.getValueAt(index,2).toString();
        txtDescription.setText(description);
        
        String status = model.getValueAt(index,3).toString();
        txtStatus.setText(status);
        
        btnUpdate.setEnabled(true);  
        btnDelete.setEnabled(true);
        btnSave.setEnabled(false);
        
        if (status.equals("approved") ) {
            btnUpdate.setEnabled(false);  
            btnDelete.setEnabled(false);
        }
        

    }//GEN-LAST:event_jTable1MouseReleased

