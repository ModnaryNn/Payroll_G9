    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        SQLRun objSQLRun = new SQLRun();
        String sql;
        ResultSet rs;
        
        sql = "SELECT * FROM deduction, payroll, employee WHERE deduction_payroll_id = payroll_id AND payroll_employee_id = employee_id";
        rs = objSQLRun.sqlQuery(sql);
        
        try {
            while (rs.next()){
                model.addRow(new Object[]{rs.getString("deduction_id"),rs.getString("employee_name"),rs.getString("deduction_amount"),rs.getString("deduction_description")});
            }
        } catch (SQLException ex) {
            Logger.getLogger(ManageDeduction.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        btnDelete.setEnabled(false);
        btnUpdate.setEnabled(false);
    }//GEN-LAST:event_formComponentShown

