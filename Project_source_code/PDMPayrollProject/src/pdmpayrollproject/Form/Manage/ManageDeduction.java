    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        //get input
        if (validateFields("edit")){
            JOptionPane.showMessageDialog(null,"All fields are required");
        }
        else{
            int id = Integer.parseInt(txtID.getText());
            Double amount = Double.parseDouble(txtAmount.getText());
            String description = txtDescription.getText();
            //Init sql connection
            SQLRun objSQLRun = new SQLRun();
            String sql;
            ResultSet rs;
            //Run sql
            sql = "UPDATE deduction "
                    + "SET deduction_amount = '" + amount
                    + "' ,deduction_description = '" + description
                    + "' WHERE deduction_id = '" + id + "'";
            System.out.println(sql);
            objSQLRun.sqlUpdate(sql);
            JOptionPane.showMessageDialog(null,"Updated Sucessfully");
            btnResetActionPerformed(evt);
        }

    }//GEN-LAST:event_btnUpdateActionPerformed

    private void jTable1MouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseReleased
        // TODO add your handling code here:
                // TODO add your handling code here:
        int index = jTable1.getSelectedRow();
        TableModel model = jTable1.getModel();
        
        String id = model.getValueAt(index,0).toString();
        selectedID = Integer.parseInt(id);
        txtID.setText(id);
        
        String department = model.getValueAt(index,1).toString();
        txtEmployee.setText(department);
        
        String position = model.getValueAt(index,2).toString();
        txtAmount.setText(position);
        
        String deduction = model.getValueAt(index,3).toString();
        txtDescription.setText(deduction);
        
        btnUpdate.setEnabled(true);  
        btnDelete.setEnabled(true);
    }//GEN-LAST:event_jTable1MouseReleased

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int a = JOptionPane.showConfirmDialog(null,"Do you want to delete this row?","Select",JOptionPane.YES_NO_OPTION);
        if ( a==0){
            try{
                SQLRun objSQLRun = new SQLRun();
                String sql;
                
                sql="DELETE FROM deduction WHERE deduction_id='" +selectedID+"'";
                objSQLRun.sqlUpdate(sql);
                JOptionPane.showMessageDialog(null,"Deleted Sucessfully");

                setVisible(false); //you can't see me!
                dispose(); //Destroy the JFrame object
                new ManageDeduction().setVisible(true);            }
            catch (Exception e){
                JOptionPane.showMessageDialog(null,e);
            }  
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

